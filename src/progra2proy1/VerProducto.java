    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed

        boolean bandera = false;
        String detalle = "";
        Listas listas = new Listas();

        if (rbtID.isSelected()) {
            for (int i = 0; i < listas.ObtenerListaProductos().size(); i++) {
                if (listas.ObtenerListaProductos().get(i).getIDProd().equalsIgnoreCase(txfFiltro.getText())) {
                    detalle = "El detalle para el ID " + listas.ObtenerListaProductos().get(i).getIDProd()
                            + " es el siguiente:\n\n" + "Nombre: " + listas.ObtenerListaProductos().get(i).getNombre()
                            + "\nPrecio: " + listas.ObtenerListaProductos().get(i).getPrecioProd() + "\n\nLista de ingredientes:\n";
                    bandera = true;
                    for (int j = 0; j < listas.ObtenerListaProductos().get(i).getLista().size(); j++) {
                        detalle = detalle + listas.ObtenerListaProductos().get(i).getLista().get(j).toString();
                    }
                }
            }
        } else if (rbtNombre.isSelected()) {
            for (int i = 0; i < listas.ObtenerListaProductos().size(); i++) {
                if (listas.ObtenerListaProductos().get(i).getNombre().equalsIgnoreCase(txfFiltro.getText())) {
                    detalle = "El detalle para el nombre " + listas.ObtenerListaProductos().get(i).getNombre()
                            + " es el siguiente:\n\n" + "ID: " + listas.ObtenerListaProductos().get(i).getIDProd()
                            + "\nPrecio: " + listas.ObtenerListaProductos().get(i).getPrecioProd() + "\n\nLista de ingredientes:\n";
                    bandera = true;
                    for (int j = 0; j < listas.ObtenerListaProductos().get(i).getLista().size(); j++) {
                        detalle = detalle + listas.ObtenerListaProductos().get(i).getLista().get(j).toString();
                    }
    }//GEN-LAST:event_btnVerActionPerformed
            }
        }

        if (bandera) {
            JOptionPane.showMessageDialog(null, detalle);
        } else {
            JOptionPane.showMessageDialog(null, "No se encuentra el producto especificado");
        }
    }

