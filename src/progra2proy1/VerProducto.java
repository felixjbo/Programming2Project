    public void GenerarListaProductos() {

        String detalle = "";
        Listas listas = new Listas();

        for (int i = 0; i < listas.ObtenerListaProductos().size(); i++) {
            if (listas.ObtenerListaProductos().get(i).getIDProd().startsWith(ID)) {
                detalle = detalle + "ID: " + listas.ObtenerListaProductos().get(i).getIDProd() + " - Nombre: " + listas.ObtenerListaProductos().get(i).getNombre() + "\n";
            }
        }
        txaListado.setText(detalle);
    }

